pub fn meff(r: f64) -> f64 {
    if r <= 0.0 {
        return 0.0;
    }
    if r >= 1.0 {
        return 1.0;
    }
    let i = EFF_ARR.iter().position(|(ri, _)| *ri <= r).unwrap();
    let (r0, m0) = EFF_ARR[i - 1];
    let (r1, m1) = EFF_ARR[i];
    let f = (r - r0) / (r1 - r0);
    m0 + f * (m1 - m0)
}

pub fn aberr_light(xx: &mut [f64; 6], earth_vel: &[f64; 3]) {
    let mut v = [0.0; 3];
    let mut v2 = 0.0;
    for i in 0..3 {
        v[i] = earth_vel[i] / 24.0 / 3600.0 / CLIGHT * AUNIT;
        v2 += v[i] * v[i];
    }

    let ru = (xx[0] * xx[0] + xx[1] * xx[1] + xx[2] * xx[2]).sqrt();
    let b_1 = (1.0 - v2).sqrt();

    let mut f1 = 0.0;
    for i in 0..3 {
        f1 += xx[i] * v[i];
    }
    f1 /= ru;
    let f2 = 1.0 + f1 / (1.0 + b_1);

    for i in 0..3 {
        xx[i] = (b_1 * xx[i] + f2 * ru * v[i]) / (1.0 + f1);
    }
}

pub fn deflect_light(xx: &mut [f64; 6], earth_helio: &[f64; 3], planet_helio: &[f64; 3]) {
    let ru = (xx[0] * xx[0] + xx[1] * xx[1] + xx[2] * xx[2]).sqrt();
    let re = (earth_helio[0] * earth_helio[0]
        + earth_helio[1] * earth_helio[1]
        + earth_helio[2] * earth_helio[2])
        .sqrt();
    let rq = (planet_helio[0] * planet_helio[0]
        + planet_helio[1] * planet_helio[1]
        + planet_helio[2] * planet_helio[2])
        .sqrt();

    let u = [xx[0] / ru, xx[1] / ru, xx[2] / ru];
    let e = [
        earth_helio[0] / re,
        earth_helio[1] / re,
        earth_helio[2] / re,
    ];
    let q = [
        planet_helio[0] / rq,
        planet_helio[1] / rq,
        planet_helio[2] / rq,
    ];

    let uq = u[0] * q[0] + u[1] * q[1] + u[2] * q[2];
    let ue = u[0] * e[0] + u[1] * e[1] + u[2] * e[2];
    let qe = q[0] * e[0] + q[1] * e[1] + q[2] * e[2];

    let sina = (1.0 - ue * ue).sqrt();
    let sin_sunr = SUN_RADIUS / re;
    let meff_fact = if sina < sin_sunr {
        meff(sina / sin_sunr)
    } else {
        1.0
    };

    let g1 = 2.0 * HELGRAVCONST * meff_fact / CLIGHT / CLIGHT / AUNIT / re;
    let g2 = 1.0 + qe;

    for i in 0..3 {
        xx[i] = ru * (u[i] + g1 / g2 * (uq * e[i] - ue * q[i]));
    }
}
